/*
	[문제]	  	
		철수는 집에서 출발하여 학원에 들려 학교까지 걸어갔다가 돌아왔다.
  		집에서 학원까지의 거리는 300m이고,
  		학원에서 학교까지의 거리는 821m이다. 
  		철수가 왕복한 거리는 얼마인지 구하시오.
	[정답] 
		2242
*/  
public class 산술연산자1_문제04_왕복 {
	public static void main(String[] args) {		
		// 집 - 학원 300 , 학원 - 학교 821, 왕복
		System.out.println(2*(300+821));

	}
}
