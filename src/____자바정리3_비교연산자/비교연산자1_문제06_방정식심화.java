/*
	[문제]
		모두 true가 출력 되도록 변수의 값을 변경하시오.
 */
public class 비교연산자1_문제06_방정식심화 {
	public static void main(String[] args) {		
		int a = 0;		
		System.out.println(5 * a - 2 == 3 * (4 * a + 4));		
		int b = 0;		
		System.out.println(-4 * b + 4 == -2 * (b - 7));
		int c = 0;		
		System.out.println(-c -2 == -2 * (1 * c - 5));
	}
}
