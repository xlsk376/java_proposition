package ____자바정리3_비교연산자;
/*
	[문제]
		모두 true가 출력 되도록 변수의 값을 변경하시오.
 */
public class 비교연산자1_문제04_방정식2 {
	public static void main(String[] args) {		
		int a = 17;
		System.out.println(7 * 2 - a == 3 * -2 - -3);
		int b = 6;
		System.out.println(b * 5 - 25 == 5);
		int c = 6;
		System.out.println(3 * c + 2 == 20);
		int d = 4;
		System.out.println(5 * d == 2 * 10);
	}
}
