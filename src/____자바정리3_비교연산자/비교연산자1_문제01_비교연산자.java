package ____자바정리3_비교연산자;
/*
	[문제]
		모두 true가 출력 되도록 값을 변경하시오.
 */
public class 비교연산자1_문제01_비교연산자 {
	public static void main(String[] args) {	// 왼쪽변경	
		System.out.println(10 == 3); // 3
		System.out.println(3 != 3); // 1
		System.out.println(5 > 38); // 40
		System.out.println(12 < 3); // 2
		System.out.println(1 >= 11); // 12
		System.out.println(32 <= 10); // 9
	}
}